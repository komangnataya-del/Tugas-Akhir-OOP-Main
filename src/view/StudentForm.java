    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        String name= jTextFieldNama.getText();
        String cardID= jTextFieldNIK.getText();
        String NIM= jTextFieldNIM.getText();
        String study= jComboBoxProdi.getSelectedItem().toString();
        
        if (name.isEmpty() || cardID.isEmpty() || study.isEmpty() || NIM.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields ", "Input Error", JOptionPane.ERROR_MESSAGE);
        }else {
            Student student = new Student(cardID,name,NIM,study);
            try {
                
                int res;
                if(selectedNIM != null){
                    
                    res = controller.update(student,selectedNIM);
                }else{
                    res = controller.create(student);
                }
                if (res == 1) {
                    JOptionPane.showMessageDialog(this, "User created successfully ", "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.selectedNIM=null;
                    this.clearForm();
                    loadStudentData();
                }else {
                    JOptionPane.showMessageDialog(this, "Error occurred while inserting ", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                 JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

    private void jTableStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableStudentMouseClicked
        // TODO add your handling code here:
        int selectedRow = jTableStudent.getSelectedRow();
    if (selectedRow != -1) {
        jTextFieldNIM.setEditable(false);
        Object nimValue = jTableStudent.getValueAt(selectedRow, 2);
        selectedNIM = (nimValue != null) ? nimValue.toString() : "";        
        Object idValue = jTableStudent.getValueAt(selectedRow, 0);
        jTextFieldNIK.setText((idValue != null) ? idValue.toString() : "");        
        Object nameValue = jTableStudent.getValueAt(selectedRow, 1);
        jTextFieldNama.setText((nameValue != null) ? nameValue.toString() : "");      
        Object prodiValue = jTableStudent.getValueAt(selectedRow, 3);
        jComboBoxProdi.setSelectedItem((prodiValue != null) ? prodiValue.toString() : "");
    }
    }//GEN-LAST:event_jTableStudentMouseClicked

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if (selectedNIM == null) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        int res = controller.delete(selectedNIM);
        if (res == 1) {
            JOptionPane.showMessageDialog(this, "Data deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            selectedNIM = null;
            clearForm();
            try {
                loadStudentData();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Error occurred", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_jButton5ActionPerformed

