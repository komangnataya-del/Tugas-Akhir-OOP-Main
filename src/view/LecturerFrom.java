    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        String nama = jTextFieldNama.getText().trim();
        String nidn = jTextFieldNIDN.getText().trim();

if (nama.isEmpty() || nidn.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please fill in all fields", "Input Error", JOptionPane.ERROR_MESSAGE);
    return;
}

Lecturer lecturer = new Lecturer("", nama, nidn, "");
int res;
if (selectedNIDN != null) {
    res = controller.update(lecturer, selectedNIDN);
} else {
    res = controller.create(lecturer);
}

if (res == 1) {
    JOptionPane.showMessageDialog(this, "Data saved successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
    clearForm();
    loadData();
} else {
    JOptionPane.showMessageDialog(this, "Error occurred", "Error", JOptionPane.ERROR_MESSAGE);
}
    }//GEN-LAST:event_jButtonSimpanActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedNIDN == null) {
    JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}
int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
if (confirm == JOptionPane.YES_OPTION) {
    int res = controller.delete(selectedNIDN);
    if (res == 1) {
        JOptionPane.showMessageDialog(this, "Data deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        clearFrom();
        loadData();
    }
}
    }//GEN-LAST:event_jButtonDeleteActionPerformed

