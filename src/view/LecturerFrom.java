    private void jTableLecturerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableLecturerMouseClicked
        // TODO add your handling code here:
        int selectedRow = jTableLecturer.getSelectedRow();
if (selectedRow != -1) {
    jTextFieldNIDN.setEditable(false);
    jTextFieldNama.setText(jTableLecturer.getValueAt(selectedRow, 0).toString());
    jTextFieldNIDN.setText(jTableLecturer.getValueAt(selectedRow, 1).toString());
    selectedNIDN = jTableLecturer.getValueAt(selectedRow, 1).toString();
}
    }//GEN-LAST:event_jTableLecturerMouseClicked

